def fun(a,b):   #تعريف تابع و دادن دو ارگومان به ان
    if(a<0):    #شرطي که اگر ارگومان اول از عدد 0 کوچکتر بود
        print("END") #END را چاپ کند

    else:  #در غير اينصورت
        print(a) #ارگومان اول را چاپ کند
        print(b)    #ارگومان دوم را چاپ کند
        print(a-1,b)   #از ارگومان  اول يکي کم کرده و ان را به همراه ارگومان دوم چاپ کند

fun(5,5) #مقدار ارگومان ها را پنج ميزاريم

fun(-1,1)  #ارگومان اول را  منفي يک و دوم را يک ميزاريم
